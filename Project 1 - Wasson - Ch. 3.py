sideA = int(input("Input the length of side A: "))
sideB = int(input("Input the length of side B: "))
sideC = int(input("Input the length of side C: "))
if sideA == sideB == sideC:
    print("You have found an equilateral triangle.")
elif sideA != sideB != sideC:
    print("You do not have an equilateral triangle.")
    
